public class BitwiseOperator {
    public static void main(String[] args) {
        // Bitwise And
        System.out.println(1 & 0);
        System.out.println(1 | 0); // Bitwise Or
        System.out.println(10001 ^ 0); // Bitwise Xor
        System.out.println(23 << 3); // Left Shift
        System.out.println(23 >> 3); // Right Shift

    }
}
